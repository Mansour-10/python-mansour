# Python List append() Method

Names = ["ahmad", "Farooq", "Mansour"]
Names.append("Zafar")

print(Names)

# Python List clear() Method

Names = ["ahmad", "Farooq", "Mansour"]
Names.clear()

print(Names)

# Python List copy() Method

Cars = ["Benz", "toyota", "RangeRover"]

a = Cars.copy()

print(a)


# Python List count() Method

Students = ["Mansour", "Zafar", "Ahmad", "Mansour"]

x = Students.count("Mansour")

print(x)

# Python List extend() Method

m = ['mohammad', 'ahmad', 'mansour']

z = ['Fahim', 'Mansour', 'farooq']

m.extend(z)

print(m)

# Python List index() Method

list = ["Mansour", "Ahmad", "Zafar", "Jawid"]

x = list.index("Jawid")

print(x)

# Python List insert() Method

classes = ['Wais', 'Jawid', 'Asma']

classes.insert(2, "Masnour")

print(classes)


# Python List pop() Method

ss = ['PC', 'Laptop', 'Phone']

ss.pop(1)

print(ss)

# Python List remove() Method

ss = ['PC', 'Laptop', 'Phone']

ss.remove("Phone")

print(ss)

# Python List reverse() Method

ss = ['PC', 'Laptop', 'Phone']

ss.reverse()

print(ss)


# Python List sort() Method

ss = ['Zafar', 'Ahmad', 'Masour']

ss.sort()

print(ss)

